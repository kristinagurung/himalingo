#!/usr/bin/env node

/**
 * Local Vector Search for JSON dictionary files
 * Usage: node local_vector_search.js [json_file] [query]
 * Example: node local_vector_search.js backend/data/bhutia_dictionary.json "how are you"
 */

const fs = require('fs').promises;
const path = require('path');
const { pipeline, cos_sim } = require('@xenova/transformers');

// Cosine similarity
function cos_sim(a, b) {
  const dotProduct = a.reduce((sum, a_i, i) => sum + a_i * b[i], 0);
  const magA = Math.sqrt(a.reduce((sum, x) => sum + x * x, 0));
  const magB = Math.sqrt(b.reduce((sum, x) => sum + x * x, 0));
  return dotProduct / (magA * magB);
}

class LocalVectorStore {
  constructor(embeddings, metadata) {
    this.embeddings = embeddings;
    this.metadata = metadata;
  }

  async search(query, topK = 3) {
    const queryEmbedding = await this.embed(query);
    const scores = this.embeddings.map((emb, i) => ({
      index: i,
      score: cos_sim(queryEmbedding, emb)
    }));
    const top = scores
      .sort((a, b) => b.score - a.score)
      .slice(0, topK)
      .map(({index, score}) => ({
        metadata: this.metadata[index],
        score
      }));
    return top;
  }

  async embed(text) {
    const generator = await pipeline('feature-extraction', 'Xenova/all-MiniLM-L6-v2');
    const output = await generator(text, { pooling: 'mean', normalize: true });
    return Array.from(output.data);
  }
}

async function main() {
  const args = process.argv.slice(2);
  const jsonPath = args[0] || 'backend/data/bhutia_dictionary.json';
  const query = args[1] || 'hello';

  try {
    // 1. Read JSON
    const rawData = await fs.readFile(jsonPath, 'utf8');
    const data = JSON.parse(rawData);

    // 2. Prepare text + metadata
    const texts = [];
    const metadata = [];
    for (const item of data) {
      const text = item.english ? `${item.english} = ${item.bhutia}` : JSON.stringify(item);
      texts.push(text);
      metadata.push(item);
    }

    // 3. Generate embeddings
    console.log('Generating embeddings...');
    const embeddings = [];
    for (let i = 0; i < texts.length; i++) {
      const emb = await embed(texts[i]);
      embeddings.push(emb);
      process.stdout.write(`\rProgress: ${i + 1}/${texts.length}`);
    }
    console.log('\\nDone!');

    // 4. Create vector store
    const store = new LocalVectorStore(embeddings, metadata);

    // 5. Search
    console.log(`\\nSearching "${query}"...`);
    const results = await store.search(query);
    console.log('\\nTop results:');
    results.forEach((r, i) => {
      console.log(`${i + 1}. Score: ${r.score.toFixed(4)}`);
      console.log(`   ${JSON.stringify(r.metadata, null, 2)}`);
    });

  } catch (error) {
    console.error('Error:', error.message);
  }
}

// Global embed helper
async function embed(text) {
  const generator = await pipeline('feature-extraction', 'Xenova/all-MiniLM-L6-v2');
  const output = await generator(text, { pooling: 'mean', normalize: true });
  return Array.from(output.data);
}

main();

